private boolean darkMode = false;
    private void btnToggleThemeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnToggleThemeActionPerformed
        // TODO add your handling code here:
        try {
            
        if (darkMode) {
            UIManager.setLookAndFeel(new FlatLightLaf());
        } else {
            UIManager.setLookAndFeel(new FlatDarkLaf());
        }

        // Refresh the whole UI after changing theme
        javax.swing.SwingUtilities.updateComponentTreeUI(this);

        darkMode = !darkMode; // toggle the state
        btnToggleTheme.setText(darkMode ? "Switch to Light Mode" : "Switch to Dark Mode");

    } catch (UnsupportedLookAndFeelException ex) {
        ex.printStackTrace();
    }
    }//GEN-LAST:event_btnToggleThemeActionPerformed

    private void btnBackspaceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBackspaceActionPerformed
        // TODO add your handling code here:
        // Get the current text from display
    String text = txtDisplay.getText();

    // Check if text is not empty
    if (text.length() > 0) {
        // Remove last character
        text = text.substring(0, text.length() - 1);
        txtDisplay.setText(text);
    }
    }//GEN-LAST:event_btnBackspaceActionPerformed

