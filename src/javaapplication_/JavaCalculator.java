    private void btnBinActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBinActionPerformed
        // TODO add your handling code here:
         // Convert current number to Binary
    try {
        // Parse the display as an integer
        int value = (int) Double.parseDouble(txtDisplay.getText());
        
        // Convert to Binary
        String binValue = Integer.toBinaryString(value);
        
        // Show result
        txtDisplay.setText(binValue);
    } catch (NumberFormatException e) {
        txtDisplay.setText("Error");
    }
    }//GEN-LAST:event_btnBinActionPerformed

